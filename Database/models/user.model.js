import { DataTypes } from "sequelize";
import sequelize from "../connedtion.js";




const userModel = sequelize.define('User', {
    username: {
        type: DataTypes.STRING,
        allowNull: false,
        unique: true
    },
    password: {
        type: DataTypes.STRING,
        allowNull: false
    },
    role: {
        type: DataTypes.STRING,
        allowNull: false,
        defaultValue: 'user'
    }
});
sequelize.sync()


export default userModel